const {
    getBloodgrp,
    getBloodgrpWiseReport
} = require('../bloodgrpReport/bloodgrpReport.service')

module.exports = {
    getBloodgrp: (req, res) => {
        getBloodgrp((err, results) => {
            if (err) {
                return res.status(200).json({
                    success: 0,
                    message: err
                });
            }

            if (!results) {
                return res.status(200).json({
                    success: 2,
                    message: "No Results Found"
                });
            }

            return res.status(200).json({
                success: 1,
                data: results
            });
        });
    },
    getBloodgrpWiseReport: (req, res) => {
        const body = req.body
        getBloodgrpWiseReport(body, (err, results) => {
            if (err) {
                return res.status(200).json({
                    success: 0,
                    message: err
                });
            }

            if (!results) {
                return res.status(200).json({
                    success: 2,
                    message: "No Results Found"
                });
            }

            return res.status(200).json({
                success: 1,
                data: results
            });
        });
    },
}